package com.lloydsbanking.salsa.apapca.service.fulfil.orderaccess.downstream;


public enum C808ErrorCodes {
    ACCOUNT_DETAILS_NOT_FOUND("211523"),
    CUSTOMER_BLOCKED("215915"),
    NO_OPEN_ACCOUNTS_FOR_THE_CUSTOMER("215916"),
    ALL_ACCOUNTS_BLOCKED_FOR_THE_CUSTOMER("215917"),
    ALL_ACCOUNTS_DORMANT_FOR_THE_CUSTOMER("215918"),
    ACCOUNT_SELECTED_IS_NOT_OPENED("215921"),
    CARD_CANNOT_BE_ISSUED_ON_ANY_ACCOUNT("215919"),
    ACCOUNT_SELECTED_IS_BLOCKED("215922"),
    ACCOUNT_SELECTED_IS_DORMANT("215923"),
    CARD_CANNOT_BE_ISSUED_ON_SELECTED_ACCOUNT("215924"),
    PARTY_BLOCKED("219305"),
    CBS_ACCOUNT_REJECTED("219306"),
    CARD_ORDER_REJECTED("215002"),
    CBS_CUSTOMER_DETAILS_NOT_FOUND("219302"),
    CBS_ACCOUNT_CLOSED("219303"),
    CBS_SYSTEM_BUSY("219304"),
    REQUEST_DATA_ERROR_IN_C808("218008"),
    RESPONSE_DATA_ERROR_IN_C808("218108"),
    UNEXPECTED_ERROR_CONDITION_IN_C808("218208"),
    EXTERNAL_SYSTEM_ID_INVALID("218900"),
    EXTERNAL_SYSTEM_ID_UNAUTHORIZED("218901"),
    USER_ID_NOT_SUPPLIED("218980"),
    USER_ID_INVALID("218981"),
    USER_ID_EXCEEDING_EIGHT_CHARACTERS("218982"),
    USER_ID_TYPE_CODE_NOT_SUPPLIED("218983"),
    USER_ID_TYPE_CODE_INVALID("218984"),
    CHANNEL_OUTLET_ID_NOT_SUPPLIED("218985"),
    CHANNEL_OUTLET_ID_INVALID("218986"),
    CHANNEL_OUTLET_ID_EXCEEDING_EIGHT_CHARACTERS("218987"),
    CHANNEL_OUTLET_TYPE_CODE_NOT_SUPPLIED("218988"),
    CHANNEL_OUTLET_TYPE_CODE_INVALID("218989"),
    MAXIMUM_REPEAT_GROUP_COUNT_INVALID("218990"),
    CIAO_API_COULD_NOT_CALL_CMAS("218997"),
    CIAO_API_FAILURES("218998"),
    CMAS_RETURNED_ERROR_CODE_TO_CIAO_API("218999");

    private String errorCode;

    C808ErrorCodes() {
        this.errorCode = null;
    }

    C808ErrorCodes(String errorCode) {
        this.errorCode = errorCode;
    }

    public static boolean isExternalErrorForC808(int errorCode) {
        for (C808ErrorCodes c808ErrorCodes : C808ErrorCodes.values()) {
            if (Integer.parseInt(c808ErrorCodes.errorCode) == errorCode) {
                return Boolean.TRUE;
            }
        }
        return Boolean.FALSE;
    }

    public String getCode() {
        return errorCode;
    }
}
